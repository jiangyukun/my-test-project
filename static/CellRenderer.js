class CellRenderer extends mxCellRenderer {
  initializeLabel(state) {
      if (state.text != null)
      {
          state.text.replaceLinefeeds = mxUtils.getValue(state.style, 'nl2Br', '1') != '0';
      }
      
      super.initializeLabel(...arguments);
  }
}

